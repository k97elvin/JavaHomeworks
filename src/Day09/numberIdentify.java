package Day09;

public class numberIdentify {
    public static void main(String[] args) {
        int a = 0;
        if (a > 0) {
            System.out.println(a + " is positive.");
        } else if (a < 0) {
            System.out.println(a + " is negative");
        } else if (a == 0) {
            System.out.println("\"a\" is equal zero");
        }
    }
}
