package Day18_NestedLoop;

public class NestedLoop {
    public static void main(String[] args) {
        for (int j = 0; j < 2; j++) { //j = 0, 1, 2

            for (int i = 0; i < 3; i++) {
                System.out.print("* ");
            }

            System.out.println();
        }
    }
}

